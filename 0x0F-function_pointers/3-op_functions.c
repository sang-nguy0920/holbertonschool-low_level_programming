#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 *op_add - sums two ints
 *
 * @a: int a
 * @b: int b
 * Return: sum
 */

int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub - subtract two ints
 *
 * @a: int a
 * @b: int b
 * Return: result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_mul - multiply two ints
 *
 * @a: int a
 * @b: int b
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_div - divide two ints
 *
 * @a: int a
 * @b: int b
 * Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mod - modulo two ints
 *
 * @a: int a
 * @b: int b
 * Return: result
 */
int op_mod(int a, int b)
{
return (a % b);
}
